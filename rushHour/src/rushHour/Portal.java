package rushHour;

/*
 * Portal object. It is in pair. i1 and j1 is the location of the first portal, i2 and j2 is the location of the second portal
 * The first one is the entrance and the second one is the exit portal.
 */
public class Portal {

	int i1, i2, j1, j2;

	public Portal(int i1, int i2, int j1, int j2) {
		this.i1 = i1;
		this.i2 = i2;
		this.j1 = j1;
		this.j2 = j2;
	}

}
